public class Switch{
    public static void main(String[] args){
        String s = "1";
        switch(s){
            case "1":
                System.out.println("s is 1");
                break;
            case "2":
                System.out.println("s is 2");
                break;
            default:
                System.out.println("s is not 1 or 2");
        }
    }
}